#include<stdio.h>
int main() {
    int a,b;
    float c,d;

    printf("Enter the value of a ");
    scanf("%d",&a);
    printf("Enter the value of b ");
    scanf("%d",&b);

    printf("Enter the value of c ");
    scanf("%f",&c);
    printf("Enter the value of d ");
    scanf("%f",&d);

    printf("++a = %d \n",++a);
    printf("--b = %d \n",--b);
    printf("++c = %f \n",++c);
    printf("--d = %f \n",++d);
}